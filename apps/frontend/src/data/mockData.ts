// ─── Appointment Slots ────────────────────────────────────────────────────────
const generateSlots = (teacherId: string, eventId: string, dates: string[]): AppointmentSlot[] => {
  const slots: AppointmentSlot[] = []
  const times = ['08:00','08:20','08:40','09:00','09:20','09:40','10:00','10:20','10:40','11:00','11:20','11:40']
  let idx = 0
  for (const date of dates) {
    for (const time of times) {
      slots.push({
        id: `slot-${teacherId}-${idx++}`,
        teacherId,
        eventId,
        time,
        date,
        status: time === '09:20' ? 'booked' : 'available',
      })
    }
  }
  return slots
}

